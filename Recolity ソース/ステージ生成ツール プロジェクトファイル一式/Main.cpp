HRESULT Init(HINSTANCE hInstance, HWND hWnd, BOOL bWindow)
{	


	// レンダリング処理の初期化
	InitRenderer(hInstance, hWnd, bWindow);
	LoadMaterial();
	// 入力処理の初期化
	InitInput(hInstance, hWnd);

	// スプライトの初期化
	InitSprite();

	// 頂点管理の初期化処理
	InitPolygon();
	Initparticle();

	return S_OK;
}


//=============================================================================
// 終了処理
//=============================================================================
void Uninit(void)
{
	// 頂点管理の終了処理
	UninitPolygon();

	// スプライトの終了処理
	UninitSprite();

	// テクスチャの全解放
	UninitTexture();

	// 入力処理の終了処理
	UninitInput();
	Uninitparticle();

	// レンダリングの終了処理
	UninitRenderer();
}

//=============================================================================
// 更新処理
//=============================================================================
void Update(void)
{
	// 入力処理の更新処理
	UpdateInput();
	Updateparticle();

	// 頂点管理の更新処理
	UpdatePolygon();
}

//=============================================================================
// 描画処理
//=============================================================================
void Draw(void)
{
	// バックバッファクリア
	Clear();

	// マトリクス設定
	SetWorldViewProjection2D();//座標の2D変換

	// 2D描画なので深度無効
	SetDepthEnable(false);

	//フィルタリングモード：ポイントフィルタ（フィルタリング無し　ピクセルがくっきり見える）
	SetSamplerState(FILTER_MODE_POINT, ADDRESS_MODE_WRAP);
	// 頂点管理の描画処理
	DrawPolygon();
	Drawparticle();

	// バックバッファ、フロントバッファ入れ替え
	Present();
}

