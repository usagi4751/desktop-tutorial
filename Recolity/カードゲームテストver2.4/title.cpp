//*****************************************************************************
// グローバル変数
//*****************************************************************************
float g_Blink = 0.0f;

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitTitle(void)
{
	if (LoadShareSound("menu")) {
		PlaySound("menu", -1, 1.0f);
	}

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitTitle(void)
{
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateTitle(void)
{
	//点滅の更新
	g_Blink += 0.02f;
	if (g_Blink > D3DX_PI*2.0f)g_Blink = 0;

	if (IsMouseLeftTriggered())
	{
		SceneTransition(SCENE_MENU);
	}
#ifdef _DEBUG
	if (GetKeyboardTrigger(DIK_RETURN) && GetFadeState() == FADE_NONE)
	{
		SceneTransition(SCENE_MENU);
	}
#endif // _DEBUG
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawTitle(void)
{
	//タイトル
	UI ui = UI{ {0.0f,0.0f},{SCREEN_WIDTH, SCREEN_HEIGHT},{0.0f,0.0f},{1.0f,1.0f},{1.0f,1.0f,1.0f,1.0f},0.0f };
	DrawScreenLT(ui, RefImageMaterial().m_Title);

	//操作指示
	ui = UI{ {SCREEN_WIDTH*0.5f,SCREEN_HEIGHT*0.5f+300.0f},{706.0f, 54.0f},{0.0f,0.0f},{1.0f,1.0f},{1.0f,1.0f,1.0f,fabsf(sinf(g_Blink))},0.0f };
	DrawScreen(ui, RefImageMaterial().m_ClickToStart);
}