void meshField::Init()
{
	for (int z = 0; z < MESHSIZE; z++)
	{
		for (int x = 0; x < MESHSIZE; x++)
		{
			float xpow = cosf((float)x / 10.0f) * 6.0f;
			float zpow = cosf((float)z / 3.0f) * 3.0f + cosf((float)z / 6.0f) * 9.0f;
			g_Fieldheight[x][z] = xpow + zpow;
		}
	}



	//頂点バッファ作成
	{
		for (int x = 0; x < MESHSIZE; x++)
		{
			for (int z = 0; z < MESHSIZE; z++)
			{
				m_Vertex[x][z].Position = {(x - 10) * 5.0f,g_Fieldheight[x][z],(z-10) * -5.0f};
				m_Vertex[x][z].Normal = {0.0f,1.0f,0.0f};
				m_Vertex[x][z].Diffuse = {1.0f,1.0f ,1.0f ,1.0f };
				m_Vertex[x][z].TexCoord = {(float)x,(float)z};
			}
		}
		for (int x = 1; x < MESHSIZE - 2; x++)
		{
			for (int z = 1; z < MESHSIZE - 2; z++)
			{
				XMFLOAT3 vx, vz, vn;
				vx.x = m_Vertex[x + 1][z].Position.x - m_Vertex[x - 1][z].Position.x;
				vx.y = m_Vertex[x + 1][z].Position.y - m_Vertex[x - 1][z].Position.y;
				vx.z = m_Vertex[x + 1][z].Position.z - m_Vertex[x - 1][z].Position.z;

				vz.x = m_Vertex[x][z - 1].Position.x - m_Vertex[x][z + 1].Position.x;
				vz.y = m_Vertex[x][z - 1].Position.y - m_Vertex[x][z + 1].Position.y;
				vz.z = m_Vertex[x][z - 1].Position.z - m_Vertex[x][z + 1].Position.z;
				//外積
				vn.x = vz.y * vx.z - vz.z * vx.y;
				vn.y = vz.z * vx.x - vz.x * vx.z;
				vn.z = vz.x * vx.y - vz.y * vx.x;

				//正規化（長さ１にする）
				float len = sqrtf(vn.x * vn.x * vn.y * vn.y + vn.z * vn.z);
				vn.x /= len;
				vn.y /= len;
				vn.z /= len;

				m_Vertex[x][z].Normal = vn;
			}
		}
		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(VERTEX_3D) * MESHSIZE * MESHSIZE;
		bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = m_Vertex;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_VertexBuffer);

	}
	//インデックスバッファー作成
	{
		unsigned int index[((MESHSIZE + 1) * 2) * (MESHSIZE - 1) - 2];
		int i = 0;
		for (int x = 0; x < MESHSIZE - 1; x++)
		{
			for (int z = 0; z < MESHSIZE; z++)
			{
				index[i] = x * MESHSIZE + z;
				i++;
				index[i] = (x + 1) * MESHSIZE + z;
				i++;
			}

			if (x == MESHSIZE - 2)
			{
				break;
			}

			//縮退用頂点
			index[i] = (x + 1) * MESHSIZE + (MESHSIZE - 1);
			i++;
			index[i] = (x + 1) * MESHSIZE;
			i++;
		}
		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(unsigned int) * (((MESHSIZE + 1) * 2) * (MESHSIZE -1) - 2);
		bd.BindFlags = D3D11_BIND_INDEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = index;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_IndexBuffer);


	}

	Renderer::CreateVertexShader(&m_VertexShader, &m_VertexLayout,
		"shader\\unlitTextureVS.cso");
	Renderer::CreatePixelShader(&m_PixelShader,
		"shader\\unlitTexturePS.cso");
}

void meshField::Uninit()
{
	m_VertexBuffer->Release();
	m_IndexBuffer->Release();
	m_VertexLayout->Release();
	m_VertexShader->Release();	
	m_PixelShader->Release();
}

