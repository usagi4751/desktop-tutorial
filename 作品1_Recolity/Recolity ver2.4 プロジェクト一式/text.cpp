#include "text.h"

//*****************************************************************************
// グローバル変数
//*****************************************************************************
DirectWrite* Write;

//=============================================================================
// 初期化処理
//=============================================================================
void InitText(void)
{
	FontData* fodata = new  FontData();
	fodata->fontSize = 15;
	fodata->fontWeight = DWRITE_FONT_WEIGHT_BOLD;
	Write = new DirectWrite(fodata);
	Write->TextInit();
	delete fodata;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitText(void)
{
	delete Write;
}

//=============================================================================
// テキストセット
//=============================================================================
void SetText(D2D1_RECT_F rect, std::string text) {

	Write->DrawString(text, rect, D2D1_DRAW_TEXT_OPTIONS_CLIP);
}